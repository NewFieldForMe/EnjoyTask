import UIKit

extension UIColor {
    static var definedBlack: UIColor {
        return UIColor(displayP3Red: 0, green: 0, blue: 0, alpha: 0.82)
    }

    static var definedDisabledBlack: UIColor {
        return UIColor(displayP3Red: 0, green: 0, blue: 0, alpha: 0.32)
    }
}
